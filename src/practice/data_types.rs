fn main() {
    let x:u32=10; // u means unsigned variable
    println!("the value of x: {}", x);

    let y:i32=-10; // u means signed variable
    println!("the value of y: {}", y);

    let f: f32 = 3.0;
    println!("the value of f: {}", f); //floating point number

    let sum = 5 + 10;
    println!("{}",sum);
    // subtraction
    let difference = 95.5 - 4.3;
    println!("{}",difference);

    // multiplication
    let product = 4 * 30;
    println!("{}",product);
    // division
    let quotient = 56.7 / 32.2;
    println!("{}",quotient);
    // remainder
    let remainder = 43 % 5;
    println!("{}",remainder);
}